    private void IgualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_IgualMouseClicked
        int num1 = Integer.parseInt(CampoPrimerNumero.getText());
        int num2 = Integer.parseInt(CampoSegundoNumero.getText());
        String operacion = CampoOperacion.getText();
        String resultado = "";

        switch (operacion) {
            case "+":
                resultado = String.valueOf(Operaciones.sumar(num1, num2));
                break;
            case "-":
                resultado = String.valueOf(Operaciones.restar(num1, num2));
                break;
            case "*":
                
                break;
            case "/":
                resultado = Operaciones.dividir(num1, num2);
                break;
            default:
                resultado = "Operación no válida";
                break;
        }
        CampoResultado.setText(resultado); 
    }//GEN-LAST:event_IgualMouseClicked

