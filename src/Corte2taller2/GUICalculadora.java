    private void escribirNumero(String numero) {
    if (CampoPrimerNumero.getText().equals("")) {
        CampoPrimerNumero.setText(numero);
    } 
    // Si el campo PrimerNumero tiene valor y el SegundoNumero está vacío, agregar al PrimerNumero
    else if (CampoSegundoNumero.getText().equals("")) {
        CampoSegundoNumero.setText(numero);
    } 

}
private void establecerOperacion(String operacion) {
    // Si el campo Operacion está vacío, establecer la operación y cambiar el foco
    if (CampoOperacion.getText().equals("")) {
        CampoOperacion.setText(operacion);
    }
}
